/*
Programacion orientada a objetos

Clase -
Es una abstraccion que hacemos de nuestra experiencia sensible . El ser humano tiende a agrupar
seres o cosas -objetos- con caracteristicas similares en grupos -clases-.

Obejto -
Un objeto es un conjunto de atributos y metodos, un objeto se deriva de una clase.

Abstraccion -
Proceso mental de extraccion de las caracteristicas esenciales de algo, ignorando los detales superfluos.

Encapsulacion -
Proceso por el que se ocultan los detalles del soporte de las caracteristicas de una abstraccion.

Herencia -
Es donde una clase nueva se crea a partir de una clase existente, heredando todos sus atributos y metodos.

Polimorfismo -
Entendemos aquella cualidad que poseen los objetos para responder de distinto modo ante el mismo mensaje.


*/


